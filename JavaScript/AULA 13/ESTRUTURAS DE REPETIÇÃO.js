// FOR
console.log('Vai começar...')
for (var c = 1 ; c <= 10; c++){
    console.log(`Passo ${c}`)
}
console.log('Fim!')

// DO WHILE
var c = 1
do{
    console.log(`Passo ${c}`)
    c++
}while (c <= 10)

// WHILE
while (c <= 2){
    console.log(`Passo ${c}`)
    c++
}   
 